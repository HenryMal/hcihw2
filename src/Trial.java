
public class Trial {
	
    private double width;
    private double distance;
    private double id; 

    public Trial(double width, double distance, double id) {
        this.width = width;
        this.distance = distance;
        this.id = id; 
    }
    
    public double getWidth() {
    	return width;
    }
    
    public double getDistance() {
    	return distance;
    }

    public double getId() {
        return id;
    }
    
}
